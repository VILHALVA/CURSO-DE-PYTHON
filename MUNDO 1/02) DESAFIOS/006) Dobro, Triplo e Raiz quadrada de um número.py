#========= DOBRO,TRIPLO E RAÍZ QUADRADA DE UM NÚMERO: =================================================================
n = float(input("Digite um número:"))

d = (n*2)
t = (n*3)
r = (n**(1/2))

print("⭐O dobro de {} vale {}.\n⭐O triplo de {} vale {}.\n⭐A raiz² de {} vale {:.2f}.".format(n, d, n, t, n, r))