# Comandos de Entrada (Input) e Operadores Aritméticos

# Comandos de Entrada (Input)
# Recebendo dois números inteiros do usuário
a = int(input("Digite o valor de 'a': "))
b = int(input("Digite o valor de 'b': "))

# Operadores Aritméticos
# Realizando várias operações aritméticas com os números fornecidos
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b

# Exibindo os resultados das operações
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)
print("Resto da divisão:", resto)
